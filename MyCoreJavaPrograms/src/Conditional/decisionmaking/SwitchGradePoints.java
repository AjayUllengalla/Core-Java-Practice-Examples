package Conditional.decisionmaking;

public class SwitchGradePoints {
	public static void main(String[] args) {
		
		int marks=81;
		char grade;
		if(marks >= 90) {
			grade = 'A';
		}else if(marks >= 75) {
			grade = 'B';
		} else if(marks >= 60) {
			grade = 'C';
		} else if(marks >= 45) {
			grade = 'D';
		}
		
		
	}

}
